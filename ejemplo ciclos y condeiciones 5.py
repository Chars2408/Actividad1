#Calcular el promedio de 5 numeros ingresado por el usuario
valor1 = float(input("Ingrese el primer número:\t")) #\t tabulación
valor2 = float(input("Ingrese el segundo número:\n")) #\n salto de línea
valor3 = float(input("Ingrese el tercer número:\n"))
valor4 = float(input("Ingrese el cuarto número:\t"))
valor5 = float(input("Ingrese el quinto número:\t"))
promedio=(valor1 + valor2 + valor3 + valor4 +valor5)/5

print ("El promedio es: ", promedio)
